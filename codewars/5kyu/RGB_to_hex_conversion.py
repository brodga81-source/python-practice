def rgb(r, g, b):
    '''
    Задача:
Преобразовать десятичные значения RGB в шестнадцатеричную строку длиной 6 символов.

Решение:
Ограничил значения каждого цветового канала диапазоном от 0 до 255.
Для верхней границы использовал min(), а для нижней — max().
Преобразовал каждый канал в шестнадцатеричный формат.
Использовал формат :02X, чтобы каждый канал занимал минимум два символа, дополнялся нулём слева и использовал заглавные буквы.
Объединил значения красного, зелёного и синего каналов в одну строку.

Что закрепил:
- ограничение чисел заданным диапазоном;
- совместное использование min() и max();
- шестнадцатеричную систему счисления;
- форматирование через :02X;
- сборку строки из нескольких форматированных значений.
    '''
    r = max(0, min(255, r))
    g = max(0, min(255, g))
    b = max(0, min(255, b))
    return f'{r:02X}{g:02X}{b:02X}'