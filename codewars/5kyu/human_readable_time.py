def make_readable(seconds):
    '''
    Задача:
Преобразовать заданное количество секунд в удобочитаемый формат HH:MM:SS.

Решение:
Вычислил количество полных часов с помощью целочисленного деления на 3600.
Получил минуты из остатка секунд после выделения полных часов.
Получил оставшиеся секунды с помощью остатка от деления на 60.
Сформировал строку в формате HH:MM:SS с помощью f-строки.
Использовал форматирование :02d, чтобы каждое значение занимало минимум две позиции и при необходимости дополнялось нулём слева.

Что закрепил:
- целочисленное деление //;
- получение остатка через %;
- перевод секунд в часы, минуты и секунды;
- форматирование целых чисел в f-строках;
- дополнение чисел нулями через :02d.
    '''
    hours = seconds // 3600
    minutes = seconds % 3600 // 60
    secs = seconds % 3600 % 60
    return f'{hours:02d}:{minutes:02d}:{secs:02d}'

print(make_readable(86399))


