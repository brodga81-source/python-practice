def rot13(message):
    '''
    Задача:
Реализовать шифр ROT13.

Каждая латинская буква должна сдвигаться на 13 позиций по алфавиту. Алфавит считается циклическим: после буквы z отсчёт продолжается с буквы a.

Необходимо:
- преобразовывать строчные английские буквы;
- преобразовывать заглавные английские буквы;
- сохранять регистр;
- оставлять цифры, пробелы и специальные символы без изменений;
- не использовать встроенный метод encode().

Пример:
"Hello, World! 123"
→
"Uryyb, Jbeyq! 123"

Решение:
Прохожу по каждому символу исходной строки.

Английский алфавит разделён на две половины:
- символы от a до m и от A до M сдвигаются вперёд на 13 позиций;
- символы от n до z и от N до Z сдвигаются назад на 13 позиций.

Сдвиг назад для второй половины даёт тот же результат, что и циклический сдвиг вперёд через конец алфавита.

Для преобразования использую:
- ord() — получение числового кода символа;
- chr() — преобразование числового кода обратно в символ.

Все символы, не входящие в диапазоны английских букв, добавляются в результат без изменений.

Что закрепил:
- работу с символами через ord() и chr();
- сравнение символов по диапазонам;
- обработку строчных и заглавных букв;
- построение новой строки через список и ''.join();
- использование цепочки if / elif / else;
- принцип работы циклического сдвига ROT13.
    '''
    result = []
    for ch in message:
        if 'a' <= ch <= 'm':
            result.append(chr(ord(ch) + 13))
        elif 'n' <= ch <= 'z':
            result.append(chr(ord(ch) - 13))
        elif 'A' <= ch <= 'M':
            result.append(chr(ord(ch) + 13))
        elif 'N' <= ch <= 'Z':
            result.append(chr(ord(ch) - 13))
        else:
            result.append(ch)
    return ''.join(result)


print(rot13('abc'))
