def array_diff(a, b):
    '''
    Задача:
    Удалить из первого списка все вхождения элементов, которые присутствуют во втором списке, сохранив исходный порядок.

    Решение:
    Создал пустой список для результата.
    Прошёл циклом по каждому элементу первого списка.
    С помощью оператора not in проверил отсутствие элемента во втором списке.
    Добавил подходящие элементы в результирующий список методом append().
    Вернул отфильтрованный список.

    Что закрепил:
    - перебор элементов списка циклом for;
    - оператор not in;
    - фильтрацию элементов списка;
    - метод append();
    - сохранение исходного порядка элементов;
    - понимание работы continue во вложенных циклах.
    '''
    result = []
    for i in a:
        if i not in b:
            result.append(i)
    return result
