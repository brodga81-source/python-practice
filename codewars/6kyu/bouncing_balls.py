def bouncing_ball(h, bounce, window):
    '''
    Задача:
Определить, сколько раз мяч пролетит мимо окна после падения и последующих отскоков.

Решение:
Сначала проверил корректность входных данных. Если хотя бы одно из условий задачи нарушено, функция возвращает -1.

Если данные корректны, считаю первое падение мяча как одно прохождение мимо окна.

Далее в цикле уменьшаю высоту мяча после каждого отскока. Пока мяч отскакивает выше уровня окна, он проходит мимо окна дважды: при движении вверх и при падении вниз, поэтому увеличиваю счетчик на 2.

Когда высота очередного отскока становится меньше либо равна высоте окна, цикл завершается и возвращается итоговое количество наблюдений.

Что закрепил:
- логические условия;
- составные проверки через `or`;
- цикл `while`;
- моделирование процесса с изменяющимся состоянием;
- пошаговую проверку алгоритма.
    '''
    if h <= 0 or bounce <= 0 or bounce >= 1 or window >= h:
        return -1
    count = 1
    while h > window:
        h *= bounce
        if h > window:
            count += 2
    return count