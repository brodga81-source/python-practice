def solution(s):
    '''
    Задача:
Разделить строку, записанную в стиле camelCase, добавив пробел перед каждой заглавной буквой.

Примеры:
"camelCasing" → "camel Casing"
"identifier" → "identifier"
"" → ""

Решение:
Создал пустой список для хранения символов результата.

Прохожу по каждому символу исходной строки:
- если символ является заглавной буквой, сначала добавляю в список пробел;
- затем добавляю сам символ.

После завершения цикла объединяю все элементы списка в одну строку с помощью метода ''.join().

Пустая строка обрабатывается автоматически: цикл не выполняется, а объединение пустого списка возвращает пустую строку.

Что закрепил:
- перебор символов строки с помощью цикла for;
- проверку регистра символа методом isupper();
- построение новой строки через список;
- добавление элементов методом append();
- объединение списка символов с помощью ''.join();
- упрощение условий за счёт вынесения повторяющегося кода за пределы if.
    '''
    result = []
    for ch in s:
        if ch == ch.upper():
            result.append(' ')
        result.append(ch)

    return ''.join(result)

print(solution("camelCasing"))