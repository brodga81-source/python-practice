def delete_nth(order,max_e):
    '''
    Задача:
Создать новый список, в котором каждый элемент встречается не более заданного количества раз, сохранив исходный порядок.

Решение:
Создал пустой результирующий список.
Последовательно прошёл по элементам исходного списка.
Проверял количество текущего элемента в результирующем списке.
Добавлял элемент только в том случае, если допустимый лимит ещё не достигнут.
Вернул сформированный список.

Что закрепил:
- сохранение порядка элементов;
- подсчёт повторений методом count();
- ограничение количества элементов;
- отличие проверки < от <=;
- последовательное формирование нового списка.
    '''
    result = []
    for i in order:
        if result.count(i) < max_e:
            result.append(i)
    return result