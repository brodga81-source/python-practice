def high(x):
    '''
    Задача:
Найти слово с наибольшей суммой позиций его букв в английском алфавите.

Решение:
Разделил исходную строку на отдельные слова.
Для каждого слова вычислил сумму значений букв.
Получал позицию буквы с помощью ord() и смещения на 96.
Сравнивал балл текущего слова с максимальным найденным баллом.
При новом максимуме сохранял слово и его результат.
Использовал строгое сравнение, чтобы при равных баллах сохранялось первое слово.

Что закрепил:
- разделение строки методом split();
- вложенные циклы;
- преобразование букв в позиции алфавита через ord();
- поиск максимального значения;
- сохранение связанного с максимумом элемента;
- обработку равных результатов.
    '''
    result = ''
    total_count = 0
    for word in x.split():
        count = 0
        for ch in word:
            count += int(ord(ch)) - 96
        if count > total_count:
            result = word
            total_count = count
    return result

print(high('aaa bbb ccc'))


