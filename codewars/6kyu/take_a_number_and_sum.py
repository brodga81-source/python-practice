def sum_dig_pow(a, b):
    '''
    Codewars: Take a Number And Sum Its Digits Raised To The Consecutive Powers And .... Eureka!!

Задача:
Реализовать функцию, которая находит все числа в заданном диапазоне, равные сумме своих цифр, возведенных в последовательные степени, начиная с единицы.

Решение:
- Выполняется проход по всем числам заданного диапазона.
- Каждое число преобразуется в строку для удобного перебора цифр.
- С помощью enumerate() одновременно получаются цифра и ее позиция.
- Каждая цифра преобразуется в целое число и возводится в степень, равную ее порядковому номеру + 1.
- Если полученная сумма совпадает с исходным числом, оно добавляется в результирующий список.
- После проверки всех чисел функция возвращает список найденных значений.
    '''
    result = []
    for number in range(a, b + 1):
        total = 0
        for index, digit in enumerate(str(number)):
            total += int(digit) ** (index + 1)
        if total == number:
            result.append(number)
    return result

print(sum_dig_pow(1, 100))
