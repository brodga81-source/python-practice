def valid_braces(string):
    '''
    Задача:
Проверить, является ли последовательность круглых, квадратных и фигурных скобок корректной.

Примеры:
"(){}[]" → True
"([{}])" → True
"(}" → False
"[(])" → False

Решение:
Использовал список как стек для хранения открывающих скобок.

Создал словарь pairs, в котором каждой закрывающей скобке соответствует её открывающая пара:

')' → '('
']' → '['
'}' → '{'

Прохожу по каждому символу строки:

- если встречается открывающая скобка, добавляю её в стек с помощью append();
- если встречается закрывающая скобка, проверяю:
  1. есть ли в стеке открывающая скобка;
  2. соответствует ли последняя открытая скобка текущей закрывающей.

Если стек пуст или скобки не соответствуют друг другу, функция сразу возвращает False.

Если скобки образуют правильную пару, удаляю последнюю открытую скобку из стека с помощью pop().

После завершения цикла проверяю, пуст ли стек:
- пустой стек → все скобки закрыты правильно → True;
- в стеке остались элементы → есть незакрытые скобки → False.

Что закрепил:
- использование списка как стека;
- принцип LIFO: последний добавленный элемент удаляется первым;
- методы append() и pop();
- получение последнего элемента через result[-1];
- проверку пустого списка через not result;
- работу со словарём;
- досрочный выход из функции через return False.
    '''
    result = []
    paris = {
        ')': '(',
        ']': '[',
        '}': '{'
    }
    for i in string:
        if i in '([{':
            result.append(i)
        else:
            if not result or result[-1] != paris[i]:
                return False
            result.pop()

    return result == []



print(valid_braces("(){}[]"))




