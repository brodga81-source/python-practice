def order(sentence):
    '''
    Задача:
Отсортировать слова в строке по цифрам, содержащимся внутри каждого слова.

Решение:
Разделил исходную строку на отдельные слова.
Создал пустой список для результата.
Прошёл по последовательным позициям от 1 до количества слов.
Для каждой позиции перебрал все слова.
Преобразовал номер позиции в строку и проверил его наличие внутри слова.
Добавил найденные слова в правильном порядке.
Объединил результат через пробел.

Что закрепил:
- метод split();
- вложенные циклы;
- функцию range();
- преобразование int в str;
- оператор in;
- метод append();
- метод join().
    '''
    words = sentence.split(' ')
    result = []
    for i in range(1, len(words) + 1):
        for word in words:
            if str(i) in word:
                result.append(word)
    return ' '.join(result)

print(order("is2 Thi1s T4est 3a"))